package co.kr.samman.dto;

public class bookmark {
	int infornum;
	int count;
	String mtitle;
	String persent;
	
	
	public String getPersent() {
		return persent;
	}
	public void setPersent(String persent) {
		this.persent = persent;
	}
	public int getInfornum() {
		return infornum;
	}
	public void setInfornum(int infornum) {
		this.infornum = infornum;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public String getMtitle() {
		return mtitle;
	}
	public void setMtitle(String mtitle) {
		this.mtitle = mtitle;
	}
	
}
